package _1010;

public class toUpperTest {
	public static void main(String[] args) {
		// 문자열 myString의 모든 알파벳을 대문자로 변환하여 리턴
		String myString = "aBcDe";
		String answer = myString.toUpperCase();
		
		System.out.println(answer);
		
		String answer2 = myString.toLowerCase();
		
		System.out.println(answer2);
	}
}
